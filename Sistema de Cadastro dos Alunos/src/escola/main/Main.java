    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        
        operacoes busca_por_aluno = new operacoes();
        int id = Integer.parseInt( id_do_aluno.getText());
        
        try {
                Aluno aluno = busca_por_aluno.consultarPorId(id);
                if(aluno.getId() == 0){
                    JOptionPane.showMessageDialog(null, "Aluno não encotrado");

                }else{
                    txt.setText("ID: " + aluno.getId() + " Nome: " + aluno.getNome() + " Turma: " + aluno.getTurma() + " Curso: " + aluno.getCurso());
                    int resposta = JOptionPane.showConfirmDialog(null, "Deseja apagar este aluno");
                    int id_aluno = aluno.getId();
                    if(resposta == JOptionPane.YES_OPTION){
                        Excluir ex = new Excluir();
                        ex.excluirAluno(id_aluno);
              
                        txt.setText("Aluno apagado com sucesso..");
                        
                    }
                }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Aluno não encotrado.....");
        }
    }//GEN-LAST:event_jButton4ActionPerformed

