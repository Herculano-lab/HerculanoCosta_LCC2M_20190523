    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String nome_do_aluno = nome.getText();
        String turma_do_aluno = turma.getText();
        String curso_do_aluno = curso.getText();
        if (nome_do_aluno.isEmpty()){
            JOptionPane.showMessageDialog(null, "Aluno Não Cadastrado ");
        }else{
        try{
            
	
            
            operacoes cadastrar = new operacoes();
            cadastrar.CadastrarAluno(nome_do_aluno, turma_do_aluno, curso_do_aluno); // Cadastrando um aluno
            JOptionPane.showMessageDialog(null, "Aluno cadstrado com sucesso.");
			
            //Limpar os campos
            nome.setText("");
            turma.setText("");
            curso.setText("");
            
            
        } catch(Exception e){
            JOptionPane.showMessageDialog(null,"Erro ao cadastrar o aluno");
        }
        
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try {
            // TODO add your handling code here:
            operacoes lista_de_alunos = new operacoes();
            
            List<Aluno> Lista_de_alunos = lista_de_alunos.consultarTodos();  // Lista de
            jFrame1.setVisible(true); 
            DefaultTableModel Dados = (DefaultTableModel) Tabela.getModel();
            Dados.setNumRows(0);
            for(Aluno a : Lista_de_alunos ){
                Dados.addRow(new Object[]{
                    a.getId(),
                    a.getNome(),
                    a.getTurma(),
                    a.getCurso(),
                });
            
            }
        } catch (SQLException ex) {
            Logger.getLogger(Main.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

